// Helper function to get all unique regions with counts
export function getRegions(): { name: string; count: number }[] {
  const regionMap = new Map<string, number>();
  wineries.forEach(w => {
    regionMap.set(w.region, (regionMap.get(w.region) || 0) + 1);
  });
  return Array.from(regionMap.entries())
    .map(([name, count]) => ({ name, count }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

// Helper function to get wineries by region
export function getWineriesByRegion(region: string): Winery[] {
  return wineries.filter(w => w.region === region);
}

// Helper function to search wineries
export function searchWineries(query: string): Winery[] {
  const lowerQuery = query.toLowerCase();
  return wineries.filter(w => 
    w.name.toLowerCase().includes(lowerQuery) ||
    w.city.toLowerCase().includes(lowerQuery) ||
    w.region.toLowerCase().includes(lowerQuery)
  );
}

export interface NearbyWinery {
  winery: Winery;
  distanceMiles: number;
}

interface NearbyOptions {
  lat: number;
  lng: number;
  radiusMiles?: number;
  limit?: number;
}

export function getNearbyWineries({
  lat,
  lng,
  radiusMiles = 25,
  limit = 50,
}: NearbyOptions): NearbyWinery[] {
  const origin = { lat, lng };
  const sanitizedRadius = Math.min(Math.max(radiusMiles, 1), 250);
  const sanitizedLimit = Math.min(Math.max(Math.floor(limit), 1), 100);

  console.log('getNearbyWineries called with:', {
    radiusMiles,
    sanitizedRadius,
    sanitizedLimit
  });

  const matches = wineries
    .filter(w => typeof w.lat === 'number' && typeof w.lng === 'number')
    .map(w => ({
      winery: w,
      distanceMiles: Number(
        calculateDistanceMiles(origin, { lat: w.lat!, lng: w.lng! }).toFixed(1)
      )
    }))
    .filter(entry => entry.distanceMiles <= sanitizedRadius)
    .sort((a, b) => a.distanceMiles - b.distanceMiles);

  console.log('Before limit - matches:', matches.length);
  const result = matches.slice(0, sanitizedLimit);
  console.log('After limit - result:', result.length);

  return result;
}